import fs from 'node:fs'
import path from 'node:path'

const rootDir = process.cwd()
const releasesDir = path.join(rootDir, 'docs', 'releases')
const outputPath = path.join(rootDir, 'lib', 'releases.generated.ts')

const releaseFiles = fs.readdirSync(releasesDir)
  .filter((file) => file.endsWith('.md'))
  .sort((a, b) => b.localeCompare(a))

const releases = releaseFiles.map((filename) => {
  const fullPath = path.join(releasesDir, filename)
  const content = fs.readFileSync(fullPath, 'utf8')
  return { filename, content }
})

const generated = `${'// This file is auto-generated by scripts/generate-releases.mjs.\n'}${'// Do not edit manually.\n\n'}export interface GeneratedRelease {\n  filename: string\n  content: string\n}\n\nexport const generatedReleases: GeneratedRelease[] = ${JSON.stringify(releases, null, 2)}\n`

fs.writeFileSync(outputPath, generated, 'utf8')
console.log(`Generated ${releases.length} releases at ${path.relative(rootDir, outputPath)}`)
